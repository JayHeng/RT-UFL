int main()
{
    rt_chip_id_t id = ufl_get_imxrt_chip_id();
	if (id == kChipId_RT6xx)
	{
		  *(uint32_t *)0x00400000 = 0xdeadbeef;
	}
	else if (id == kChipId_RT106x)
	{
		  *(uint32_t *)0x00001000 = 0xdeadbeef;
	}
	else
	{
	}

    while (1);
}
